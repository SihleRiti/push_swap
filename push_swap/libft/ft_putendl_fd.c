#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	if (str)
	{
		while (str[i])
		{
			write(fd, &str[i], 1);
			i++;
		}
		write(fd, "\n", 1);
	}
}
