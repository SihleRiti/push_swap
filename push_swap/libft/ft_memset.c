#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*s1;
	size_t	i;

	s1 = (char *)str;
	i = 0;
	while (i < n)
	{
		s1[i] = c;
		i++;
	}
	return (s1);
}
